    private void tbDoUongAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_tbDoUongAncestorAdded
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Tên đồ uống");
        model.addColumn("Số lượng");
        tbDoUong.setModel(model);
        TimerTask updateTask = new TimerTask(){
             public void run(){
                 try{
                     Connection connection = MySQLConnect.getConnection();
                     String query = "SELECT DoUong.tenDoUong, soLuong\n" +
                    "FROM Kho \n" +
                    "JOIN DoUong on DoUong.idDoUong = Kho.idDoUong ";
                     PreparedStatement statement = connection.prepareStatement(query);
                     ResultSet rs = statement.executeQuery();
                     model.setRowCount(0);
                     while(rs.next()){
                         String tenDoUong = rs.getString("tenDoUong");
                         String soLuong = rs.getString("soLuong");
                         model.addRow(new Object[]{ tenDoUong, soLuong});
                     }
                 }catch(SQLException e){
                     e.printStackTrace();
                 }
             }
        };
    Timer timer = new Timer();
    timer.scheduleAtFixedRate(updateTask,0,1000);
    }//GEN-LAST:event_tbDoUongAncestorAdded

