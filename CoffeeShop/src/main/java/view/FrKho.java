    private void btnThemActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThemActionPerformed
        if(checkEmpty()){
            JOptionPane.showMessageDialog(this, "Vui lòng nhập đầy đủ thông tin", "Error", JOptionPane.ERROR_MESSAGE); 
        }
        else{
            String tenDoUong = txtTenDoUong.getText();
            khoController = new KhoController();           
            if(khoController.checkExists(tenDoUong)){
                int idDoUong = khoController.changeIDDoUong(tenDoUong);
                int soLuong = Integer.parseInt(txtSoLuong.getText());
                kho = new Kho(idDoUong, soLuong);
                khoController.create(kho);
                JOptionPane.showMessageDialog(this, "Thêm thành công", "Success", JOptionPane.INFORMATION_MESSAGE);  
                txtTenDoUong.setText("");
                txtSoLuong.setText("");
                
            }
            else{
                JOptionPane.showMessageDialog(this, "Tên đồ uống không tồn tại", "Error", JOptionPane.ERROR_MESSAGE);           
            }
                
        }
    }//GEN-LAST:event_btnThemActionPerformed

    public boolean checkEmpty(){
        Boolean check = false;
        String soLuong = txtSoLuong.getText();
        String tenDoUong = txtTenDoUong.getText();
        if(soLuong.isEmpty() || tenDoUong.isEmpty()){
            check = true;
        }
        return check;
    }
    private void btnXoa4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnXoa4ActionPerformed

       if(txtTenDoUong4.getText().isEmpty()){
           JOptionPane.showMessageDialog(this, "Vui lòng nhập đồ uống", "Error", JOptionPane.ERROR_MESSAGE); 
       }
       else{
            String tenDoUong = txtTenDoUong4.getText();
           khoController = new KhoController();
           if(khoController.checkExists(tenDoUong)){
               khoController.delete(tenDoUong);
               JOptionPane.showMessageDialog(this, "Xoá thành công", "Success", JOptionPane.INFORMATION_MESSAGE);  
               txtTenDoUong4.setText("");
           }else{
               JOptionPane.showMessageDialog(this, "Đồ uống không tồn tại", "Error", JOptionPane.ERROR_MESSAGE); 
           }
           
       }
    }//GEN-LAST:event_btnXoa4ActionPerformed

