    private void btnThem2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnThem2ActionPerformed
        if(checkEmpty()){
            JOptionPane.showMessageDialog(this, "Không được để trống", "Error", JOptionPane.ERROR_MESSAGE); 
        }
        else{
            String tenDoUong = txtTenDoUong.getText();
            int soLuong = (int) spSoLuong.getValue();
            hoaDonController = new HoaDonController();
            if(hoaDonController.checkExist(tenDoUong)){
                String idDoUong = hoaDonController.changeIdDoUong(tenDoUong);
                hoaDon.cthd = new CTHD(idDoUong, soLuong);
                Integer idHoaDon = hoaDon.idHoaDon;
                hoaDonController.createCTHD(idHoaDon,hoaDon.cthd);
                JOptionPane.showMessageDialog(this, "Nhập thành công", "Success", JOptionPane.INFORMATION_MESSAGE);           
            }
            else{
                JOptionPane.showMessageDialog(this, "Tên đồ uống hiện không tồn tại", "Error", JOptionPane.ERROR_MESSAGE); 
            }
            
        }
    }//GEN-LAST:event_btnThem2ActionPerformed

