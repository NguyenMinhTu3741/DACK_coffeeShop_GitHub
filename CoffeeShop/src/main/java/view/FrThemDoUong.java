    private void tbCTHDAncestorAdded(javax.swing.event.AncestorEvent evt) {//GEN-FIRST:event_tbCTHDAncestorAdded
        DefaultTableModel model = new DefaultTableModel();
        model.addColumn("Đồ uống");
        model.addColumn("Số lượng");
        model.addColumn("Giá");
        tbCTHD.setModel(model);
        TimerTask updateTask = new TimerTask() {
            @Override
            public void run(){
                try{    
                    Connection connection = MySQLConnect.getConnection();
                    String query = "SELECT DoUong.tenDoUong, CTHD.soLuong, (CTHD.soLuong * DoUong.price) AS totalPrice\n" +
"				   FROM CTHD\n" +
"				   JOIN DoUong ON DoUong.idDoUong = CTHD.idDoUong\n" +
"				   WHERE CTHD.idHoaDon = ?";
                    PreparedStatement statement = connection.prepareStatement(query);
                    statement.setInt(1,hoaDon.idHoaDon);
                    ResultSet rs = statement.executeQuery();
                    model.setRowCount(0);
                    while(rs.next()){
                        String tenDoUong = rs.getString("tenDoUong");
                        String soLuong = rs.getString("soLuong");
                        Double gia = rs.getDouble("totalPrice");
                        model.addRow(new Object[]{tenDoUong, soLuong, gia});
                        
                    }
                } catch(SQLException e){
                    e.printStackTrace();
                }
            }
        };
    Timer timer = new Timer();
    timer.scheduleAtFixedRate(updateTask, 0, 1000);
    }//GEN-LAST:event_tbCTHDAncestorAdded

